# If, Elif and Else Statements

var = 2

if var < 1:
    print('This is correct')
else:
    print('This is incorrect')


var1 = 'a'

if var1 == 'b':
    print('This is a b')
elif var1 == 'c':
    print('This is a c')
else:
    print('This is a')

name = 'Frankie'

if name == 'Sam':
    print("Hello Sam")
elif name == 'Frankie':
    print('Hello Frankie')
else:
    print('We don\'t have you on the system. What is your name?')

